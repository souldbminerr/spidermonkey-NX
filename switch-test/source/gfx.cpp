bool gfx_text(JSContext *cx, unsigned argc, JS::Value *vp) {
  JS::CallArgs args = JS::CallArgsFromVp(argc, vp);
  if (!g_inited || args.length() < 3) {
    args.rval().setUndefined();
    return true;
  }
  JS::RootedString jstr(cx, JS::ToString(cx, args[0]));
  if (!jstr)
    return false;
  JS::UniqueChars str = JS_EncodeStringToUTF8(cx, jstr);
  if (!str)
    return false;
  int x0, y0;
  u32 r, g, b, a, scale;
  if (!argI32(cx, args, 1, &x0) || !argI32(cx, args, 2, &y0) ||
      !argU32(cx, args, 3, &r, 255) || !argU32(cx, args, 4, &g, 255) ||
      !argU32(cx, args, 5, &b, 255) || !argU32(cx, args, 6, &a, 255) ||
      !argU32(cx, args, 7, &scale, 2))
    return false;
  if (scale < 1)
    scale = 1;
  r &= 0xff;
  g &= 0xff;
  b &= 0xff;
  a &= 0xff;
  int x = x0, y = y0, maxw = 0;
  for (const char *p = str.get(); *p; p++) {
    unsigned char ch = (unsigned char)*p;
    if (ch == '\n') {
      if (x - x0 > maxw)
        maxw = x - x0;
      x = x0;
      y += 8 * (int)scale;
      continue;
    }
    if (ch < 0x20 || ch > 0x7F)
      ch = '?';
    const unsigned char *gl = kFont8x8[ch - 0x20];
    for (int row = 0; row < 8; row++) {
      unsigned char bits = gl[row];
      if (!bits)
        continue;
      for (int col = 0; col < 8; col++) {
        if (!((bits >> col) & 1))
          continue;
        int px = x + col * (int)scale, py = y + row * (int)scale;
        for (u32 oy = 0; oy < scale; oy++) {
          int yy = py + (int)oy;
          if (yy < 0 || yy >= (int)g_h)
            continue;
          u32 *srow = g_surface + (size_t)yy * g_w;
          for (u32 ox = 0; ox < scale; ox++) {
            int xx = px + (int)ox;
            if (xx < 0 || xx >= (int)g_w)
              continue;
            srow[xx] = blend(srow[xx], r, g, b, a);
          }
        }
      }
    }
    x += 8 * (int)scale;
  }
  if (x - x0 > maxw)
    maxw = x - x0;
  args.rval().setInt32(maxw);
  return true;
}

bool gfx_present(JSContext *cx, unsigned argc, JS::Value *vp) {
  JS::CallArgs args = JS::CallArgsFromVp(argc, vp);
  if (!g_inited) {
    args.rval().setUndefined();
    return true;
  }
  u32 stride = 0;
  u8 *fb = (u8 *)framebufferBegin(&g_fb, &stride);
  if (fb) {
    for (u32 y = 0; y < g_h; y++)
      memcpy(fb + (size_t)y * stride, g_surface + (size_t)y * g_w,
             (size_t)g_w * 4);
    framebufferEnd(&g_fb);
  }
  args.rval().setUndefined();
  return true;
}

