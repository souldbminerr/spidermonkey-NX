bool gfxIsInited() { return g_inited; }

void installGfx(JSContext *cx, JS::HandleObject nx) {
  JS::RootedObject gfx(cx, JS_NewPlainObject(cx));
  JS_DefineFunctions(cx, gfx, gfxFunctions);
  JS::RootedValue v(cx, JS::ObjectValue(*gfx));
  JS_SetProperty(cx, nx, "gfx", v);
}
