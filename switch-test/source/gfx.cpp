Framebuffer g_fb;
bool g_inited = false;
u32 g_w = 0, g_h = 0;
u32 *g_surface = nullptr;

inline u32 pack(u32 r, u32 g, u32 b, u32 a) {
  return (a << 24) | (b << 16) | (g << 8) | r;
}

inline u32 blend(u32 dst, u32 r, u32 g, u32 b, u32 a) {
  if (a >= 255)
    return pack(r, g, b, 255);
  if (a == 0)
    return dst;
  u32 dr = dst & 0xff, dg = (dst >> 8) & 0xff, db = (dst >> 16) & 0xff;
  u32 ia = 255 - a;
  u32 nr = (r * a + dr * ia) / 255;
  u32 ng = (g * a + dg * ia) / 255;
  u32 nb = (b * a + db * ia) / 255;
  return pack(nr, ng, nb, 255);
}

bool argU32(JSContext *cx, const JS::CallArgs &args, unsigned i, u32 *out,
            u32 def = 0) {
  if (i >= args.length()) {
    *out = def;
    return true;
  }
  double d;
  if (!JS::ToNumber(cx, args[i], &d))
    return false;
  if (d < 0)
    d = 0;
  *out = (u32)d;
  return true;
}

bool argI32(JSContext *cx, const JS::CallArgs &args, unsigned i, int *out,
            int def = 0) {
  if (i >= args.length()) {
    *out = def;
    return true;
  }
  double d;
  if (!JS::ToNumber(cx, args[i], &d))
    return false;
  *out = (int)d;
  return true;
}

bool gfx_init(JSContext *cx, unsigned argc, JS::Value *vp) {
  JS::CallArgs args = JS::CallArgsFromVp(argc, vp);
  if (g_inited) {
    JS::RootedObject o(cx, JS_NewPlainObject(cx));
    JS::RootedValue w(cx, JS::NumberValue(g_w)), h(cx, JS::NumberValue(g_h));
    JS_SetProperty(cx, o, "width", w);
    JS_SetProperty(cx, o, "height", h);
    args.rval().setObject(*o);
    return true;
  }
  consoleExit(nullptr);

  NWindow *win = nwindowGetDefault();
  g_w = 1280;
  g_h = 720;
  Result rc =
      framebufferCreate(&g_fb, win, g_w, g_h, PIXEL_FORMAT_RGBA_8888, 2);
  if (R_FAILED(rc)) {
    JS_ReportErrorASCII(cx, "gfx.init: framebufferCreate failed rc=0x%x",
                        (unsigned)rc);
    return false;
  }
  rc = framebufferMakeLinear(&g_fb);
  if (R_FAILED(rc)) {
    framebufferClose(&g_fb);
    JS_ReportErrorASCII(cx, "gfx.init: framebufferMakeLinear failed rc=0x%x",
                        (unsigned)rc);
    return false;
  }
  g_surface = (u32 *)malloc((size_t)g_w * g_h * 4);
  if (!g_surface) {
    framebufferClose(&g_fb);
    JS_ReportOutOfMemory(cx);
    return false;
  }
  memset(g_surface, 0, (size_t)g_w * g_h * 4);
  g_inited = true;

  JS::RootedObject o(cx, JS_NewPlainObject(cx));
  JS::RootedValue w(cx, JS::NumberValue(g_w)), h(cx, JS::NumberValue(g_h));
  JS_SetProperty(cx, o, "width", w);
  JS_SetProperty(cx, o, "height", h);
  args.rval().setObject(*o);
  return true;
}

bool gfx_deinit(JSContext *cx, unsigned argc, JS::Value *vp) {
  JS::CallArgs args = JS::CallArgsFromVp(argc, vp);
  if (g_inited) {
    framebufferClose(&g_fb);
    free(g_surface);
    g_surface = nullptr;
    g_inited = false;
    consoleInit(nullptr);
  }
  args.rval().setUndefined();
  return true;
}

bool gfx_clear(JSContext *cx, unsigned argc, JS::Value *vp) {
  JS::CallArgs args = JS::CallArgsFromVp(argc, vp);
  if (!g_inited) {
    args.rval().setUndefined();
    return true;
  }
  u32 r, g, b;
  if (!argU32(cx, args, 0, &r) || !argU32(cx, args, 1, &g) ||
      !argU32(cx, args, 2, &b))
    return false;
  u32 px = pack(r & 0xff, g & 0xff, b & 0xff, 255);
  size_t n = (size_t)g_w * g_h;
  for (size_t i = 0; i < n; i++)
    g_surface[i] = px;
  args.rval().setUndefined();
  return true;
}

bool gfx_fillRect(JSContext *cx, unsigned argc, JS::Value *vp) {
  JS::CallArgs args = JS::CallArgsFromVp(argc, vp);
  if (!g_inited) {
    args.rval().setUndefined();
    return true;
  }
  int x, y, w, h;
  u32 r, g, b, a;
  if (!argI32(cx, args, 0, &x) || !argI32(cx, args, 1, &y) ||
      !argI32(cx, args, 2, &w) || !argI32(cx, args, 3, &h) ||
      !argU32(cx, args, 4, &r) || !argU32(cx, args, 5, &g) ||
      !argU32(cx, args, 6, &b) || !argU32(cx, args, 7, &a, 255))
    return false;
  int x0 = x, y0 = y, x1 = x + w, y1 = y + h;
  if (x0 < 0)
    x0 = 0;
  if (y0 < 0)
    y0 = 0;
  if (x1 > (int)g_w)
    x1 = g_w;
  if (y1 > (int)g_h)
    y1 = g_h;
  r &= 0xff;
  g &= 0xff;
  b &= 0xff;
  a &= 0xff;
  for (int py = y0; py < y1; py++) {
    u32 *row = g_surface + (size_t)py * g_w;
    for (int px = x0; px < x1; px++)
      row[px] = blend(row[px], r, g, b, a);
  }
  args.rval().setUndefined();
  return true;
}

bool gfx_blit(JSContext *cx, unsigned argc, JS::Value *vp) {
  JS::CallArgs args = JS::CallArgsFromVp(argc, vp);
  if (!g_inited) {
    args.rval().setUndefined();
    return true;
  }
  if (args.length() < 5 || !args[0].isObject()) {
    JS_ReportErrorASCII(cx, "gfx.blit(u8, sw, sh, dx, dy, [sx, sy, a])");
    return false;
  }
  JS::RootedObject buf(cx, &args[0].toObject());
  if (!JS_IsUint8Array(buf)) {
    JS_ReportErrorASCII(cx, "gfx.blit: arg0 must be a Uint8Array");
    return false;
  }
  u32 sw, sh, sxs, sys, a;
  int dx, dy;
  if (!argU32(cx, args, 1, &sw) || !argU32(cx, args, 2, &sh) ||
      !argI32(cx, args, 3, &dx) || !argI32(cx, args, 4, &dy) ||
      !argU32(cx, args, 5, &sxs, 1) || !argU32(cx, args, 6, &sys, 1) ||
      !argU32(cx, args, 7, &a, 255))
    return false;
  if (sxs < 1)
    sxs = 1;
  if (sys < 1)
    sys = 1;
  size_t len = JS_GetTypedArrayByteLength(buf);
  if ((size_t)sw * sh * 4 > len) {
    JS_ReportErrorASCII(cx, "gfx.blit: buffer smaller than sw*sh*4");
    return false;
  }
  {
    JS::AutoCheckCannotGC nogc;
    bool shared;
    const uint8_t *data = JS_GetUint8ArrayData(buf, &shared, nogc);
    for (u32 sy = 0; sy < sh; sy++) {
      for (u32 sx = 0; sx < sw; sx++) {
        const uint8_t *p = data + ((size_t)sy * sw + sx) * 4;
        u32 r = p[0], g = p[1], b = p[2];
        u32 pa = (a * p[3]) / 255;
        if (pa == 0)
          continue;
        int bx = dx + (int)(sx * sxs);
        int by = dy + (int)(sy * sys);
        for (u32 oy = 0; oy < sys; oy++) {
          int yy = by + (int)oy;
          if (yy < 0 || yy >= (int)g_h)
            continue;
          u32 *row = g_surface + (size_t)yy * g_w;
          for (u32 ox = 0; ox < sxs; ox++) {
            int xx = bx + (int)ox;
            if (xx < 0 || xx >= (int)g_w)
              continue;
            row[xx] = blend(row[xx], r, g, b, pa);
          }
        }
      }
    }
  }
  args.rval().setUndefined();
  return true;
}

